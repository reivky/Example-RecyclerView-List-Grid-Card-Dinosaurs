package com.reivart.dinosaurus;

public class Dino {
    private String name;
    private String detail;
    private int photo;
    public String getName() {
        return name;
    }
    void setName(String name) {
        this.name = name;
    }
    public String getDetail() {
        return detail;
    }
    void setDetail(String detail) {
        this.detail = detail;
    }
    public int getPhoto() {
        return photo;
    }
    void setPhoto(int photo) {
        this.photo = photo;
    }
}